package topics.basics.operators;

public class IncrementAndDecrementOperators {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x++);
        System.out.println(x);

        System.out.println(++x);
        System.out.println(x);
        int y =10;
        System.out.println(--y);
        System.out.println(y--);
        System.out.println(y);

    }
}
