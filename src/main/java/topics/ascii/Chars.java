package topics.ascii;

public class Chars {
    public static void main(String[] args) {
        // ASCII values of chars
        // A starts from 65
        // a starts from 97
        char c1 = 'A';
        char c2 = 'a';
        System.out.println("c1 = " + (int)c1);
        System.out.println("c2 = " + (int)c2);
    }
}
